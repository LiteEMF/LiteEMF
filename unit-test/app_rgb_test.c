void app_rgb_test(void)
{
	uint16_t i;
	for(i=0; i<256; i++){
		logd("%d,", (uint8_t)sin8(i));
		if ((i&0X0F) == 0X0F) logd("\n");
	}

	logd("\n");
	for(i=0; i<256; i++){
		logd("%d, ", (uint8_t)(sin(i/128.0*3.1415926) * 127.5 + 0.5));
		if ((i&0X0F) == 0X0F) logd("\n");
	}
}


